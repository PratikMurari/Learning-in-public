//var.slice(starting position,ending position) slices the string characters and we can pick and choose the characters we want
var name = "Pratik";
//slicing first character
console.log(name.slice(0, 1));
//slicing last 3rd character
console.log(name.slice(2, 3));
//slicing last 3 characters
console.log(name.slice(-3));
// (ending position - starting position) = number of characters

